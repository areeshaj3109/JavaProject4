    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        try {
            admin a = new admin();
            if(!a.login(username.getText(), password.getText())) {
                JOptionPane.showMessageDialog(this, "Invalid Username or Password, please try again.");
            }
            else {
                if(a.removeAdmin(removeUsername.getText())) {
                    JOptionPane.showMessageDialog(this, "Admin deleted successfully.");
                    username.setText("");
                    password.setText("");
                    removeUsername.setText("");
                }
                else
                    JOptionPane.showMessageDialog(this, "No Admin with this username exists.");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(updateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(updateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        }
        
      
    }//GEN-LAST:event_removeActionPerformed

