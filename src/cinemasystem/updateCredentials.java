    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        
        try {
            if(NameTF.getText().equals("") || cnicTF.getText().equals("") || usernameTF.getText().equals("") || pswdTF.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "No field can be left blank.");
            }
            else if(cnicTF.getText().length() != 13) {
                JOptionPane.showMessageDialog(this, "Invalid CNIC Number, please try again.");
            }
            else if(!pswdTF.getText().equals(CpswdTF.getText())) {
                JOptionPane.showMessageDialog(this, "Password and Confirm Password did not matched, please try again.");
            }
            else {
                admin a = new admin(NameTF.getText(),cnicTF.getText(),usernameTF.getText(),pswdTF.getText());
                a.addAdmin();
                JOptionPane.showMessageDialog(this, "New Admin added successfully.");
                NameTF.setText("");
                cnicTF.setText("");
                usernameTF.setText("");
                pswdTF.setText("");
                CpswdTF.setText("");
            }
            
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(updateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(updateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addActionPerformed

