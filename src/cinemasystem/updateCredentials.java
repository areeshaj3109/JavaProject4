    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            admin a = new admin();
            if(!a.login(IDTF.getText(), oldPswTF.getText())) {
                JOptionPane.showMessageDialog(this, "Invalid Username or Old Password, please try again.");
            }
            else if(newPswTF.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Password can not be empty, please try again.");   
            }
            else if(!newPswTF.getText().equals(newPsw2TF.getText())) {
                JOptionPane.showMessageDialog(this, "Password and Confirm Password did not matched, please try again.");   
            }
            else {
                a.updatePassword(IDTF.getText(), newPswTF.getText());
                JOptionPane.showMessageDialog(this, "Password updated successfully.");
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(updateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(updateCredentials.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateActionPerformed

